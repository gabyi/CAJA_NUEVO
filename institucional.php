            <div class="col-md-12">
                <h2>Coordinadora de Cajas</h2>
                <div class="col-md-12">
                    <br>
                    <h3>Inicios</h3>
                    <br>
                        El 14 de marzo de 1980 fue sancionada la ley 22193, mediante la cual se introdujeron reformas 
                        al régimen básico para trabajadores autónomos regido por las leyes 18038 y 18826.

                        Como esta reforma dio lugar a múltiples especulaciones sobre el destino, funcionamiento, 
                        administración y alcance de las treinta Cajas para Profesionales entonces existentes, se abrió una 
                        corriente espontánea de acercamiento entre estas entidades tendiente a la búsqueda de soluciones comunes 
                        que resguardaran su autonomía e idiosincrasia. A tal efecto, se decidió la convocatoria a las III Jornadas de 
                        Cajas de previsión de Profesionales Universitarios de la República Argentina. Este histórico encuentro se 
                        concretó en la ciudad de Santa Fe en el mes de marzo de 1980 y convocó masivamente a la dirigencia de las Cajas.

                        De allí surgió la necesidad de constituir una Comisión representativa de todas esas Cajas para conducir la actividad 
                        que imponía la ley nacional, la que requería la participación de las Cajas, conjuntamente con el Estado Nacional, en 
                        la elaboración de un convenio de reciprocidad jubilatoria. Al mismo tiempo, se fueron requiriendo y obteniendo de las
                        autoridades competentes de cada provincia el dictado de las normas jurídicas que en definitiva convalidaron el 
                        convenio así incorporado al derecho público provincial. Entonces, ese mismo año y conforme a la ley 22193, las Cajas 
                        celebraron un convenio que fue aprobado en una reunión extraordinaria en la ciudad de San Miguel de Tucumán.

                        Un año después, en 1981 se detectó la necesidad de mantener los vínculos entre las principales entidades para asumir nuevas 
                        responsabilidades, ampliar objetivos, coordinar esfuerzos e impulsar el advenimiento de nuevos sistemas en las provincias y 
                        para las profesiones que carecían de ello; así, en Salta, el 22 de agosto, se fundó la Coordinadora de Cajas de Previsión y 
                        Seguridad Social para Profesionales de la República Argentina.

                        Transcurridos 28 años, la labor de la Coordinadora creció incesantemente y se llevaron a cabo emprendimientos de la más variada 
                        índole, los cuales consolidan la razón de su creación y le otorgan una auténtica representatividad. En la actualidad, la 
                        Coordinadora reúne a más de 70 Cajas. Autoridades.

                        Según el Estatuto, el Plenario de la Coordinadora -su órgano de conducción- designa anualmente a una Caja para que desempeñe 
                        la Secretaría General, órgano ejecutivo de las decisiones del Plenario y realizador de los planes y programas adoptados en 
                        función de sus objetivos. Asimismo, designa a los nueve miembros del Comité Ejecutivo, creado para colaborar con la Secretaria 
                        General y actuar junto a ésta en cuestiones extraordinarias.
                        La Caja Forense en la Coordinadora de Cajas

                        A partir de la designación del Dr. Juan Víctor Bensusan como Secretario General, la Caja Forense de La Pampa desempeñó la 
                        Secretaría General de la Coordinadora durante el período de 1993-1995. Sin dudas, el haber sido una de las Cajas fundadoras y 
                        la permanente colaboración y actividad, fueron aspectos que avalaron este importante nombramiento.

                        Estos años de intensa gestión estuvieron signados por la reorganización institucional, procurando más eficiencia y participación 
                        de las Cajas Miembros, y una importante y exitosa actividad desarrollada en pos de la defensa de la existencia misma de las Cajas 
                        de Previsión y Seguridad Social para Profesionales de todo el país. En este marco, algunos de los logros obtenidos fueron:

                        La actualización del Estatuto de la Coordinadora.
                        La no obligatoriedad del Sistema Integrado de Jubilaciones y Pensiones para los profesionales afiliados a sus propias Cajas de Previsión.

                        El bloqueo a la iniciativa del Poder Ejecutivo Nacional de gravar con el 21% los ingresos de los profesionales del interior del país que 
                        no se sometieran voluntariamente a la doble aportación, adhiriendo también al régimen nacional. La incorporación al texto del Constitución 
                        Nacional de la segunda parte del artículo 125, donde se reconoce expresamente la soberanía provincial en materia de seguridad social para 
                        profesionales.

